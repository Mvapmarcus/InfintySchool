# Prova Infinity (Variaveis, Entrada e Saída): Cálculo da área de um retângulo
# Pede ao usuário a base e a altura do retângulo e calcula a área   

base = float(input('Digite o valor da base do retângulo:'))
altura = float(input('Digite o valor da altura do retângulo:'))

print(f'o valor da área do retangulo é = {base*altura}')